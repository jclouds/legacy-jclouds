package org.jclouds.trmk.vcloud_0_8.domain;

import java.net.URI;

/**
 * @author Adrian Cole
 */
public class PublicIpAddress implements Comparable<PublicIpAddress> {
   private final String address;
   private final URI id;

   public PublicIpAddress(String address, URI id) {
      this.address = address;
      this.id = id;
   }

   public URI getId() {
      return id;
   }

   public String getAddress() {
      return address;
   }

   public int compareTo(PublicIpAddress that) {
      return (this == that) ? 0 : getId().compareTo(that.getId());
   }

   @Override
   public int hashCode() {
      final int prime = 31;
      int result = 1;
      result = prime * result + ((address == null) ? 0 : address.hashCode());
      result = prime * result + ((id == null) ? 0 : id.hashCode());
      return result;
   }

   @Override
   public boolean equals(Object obj) {
      if (this == obj)
         return true;
      if (obj == null)
         return false;
      if (getClass() != obj.getClass())
         return false;
      PublicIpAddress other = (PublicIpAddress) obj;
      if (address == null) {
         if (other.address != null)
            return false;
      } else if (!address.equals(other.address))
         return false;
      if (id == null) {
         if (other.id != null)
            return false;
      } else if (!id.equals(other.id))
         return false;
      return true;
   }

   @Override
   public String toString() {
      return "PublicIpAddress [address=" + address + ", id=" + id + "]";
   }

}
